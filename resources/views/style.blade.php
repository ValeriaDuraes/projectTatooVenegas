        <div class="title-history"> Sailor Jerry: A História de Norman Collins, O Marinheiro</div> <br>
        Isso mesmo, as tattoos Old School, surgiram a partir de um jovem marinheiro americano, conhecido como ”Sailor Jerry” , ou oficialmente conhecido como Norman Collins, (1911- 1973), seu nome de registro em cartório. Seus desenhos eram conhecidos por ter traços bem grossos e pretos – comuns do estilo Old School – e cores primárias como o azul, o amarelo e vermelho, uma vez que, naquela época, não haviam muitas tintas disponíveis para tatuagem. O resto das cores, ele fazia misturando as cores primárias para originar o verde, o laranja e o roxo.

        *Curiosidade: Jerry, foi apelidado pelo pai, por seu humor rabugento, semelhante à Mula que era o pet da família – KK WTF

        Norman, alistou-se em 1930 para a marinha americana, quando atravessou os 07 mares, navegando até a China e o Japão. Foi lá que ele teve seu primeiro contato com as artes de origem oriental, que serviram de influência para construção do seu estilo de desenho. Apesar disso, Old School é um estilo de tatuagem tradicional do ocidente.

        Após alguns problemas na marinha, sofreu uma restrição e abandonou o grupo. Foi quando, em 1942, ainda durante a Segunda Guerra Mundial (1939-1945), que mudou-se para o Havaí e começou a trabalhar profissionalmente como um tatuador. Em 1950, mesmo após o conflito entre as sociedades dos EUA e Japão, Jerry começou a trocar cartas com artistas de tatuagem japoneses, em busca de aperfeiçoar suas técnicas de desenho e conhecer mais sobre os estilos de tatuagens asiáticas. 

        Sailor Jerry, como bom marinheiro, era apaixonado por barcos e navios, e seus primeiros trabalhos de tatuagem, foram feitos por hobby, tatuando as embarcações nos braços de soldados da marinha. Graças à objetos da natureza, pertencentes ao repertório de uma rotina de um marinheiro em alto mar, assim como vemos de maneira recorrente ainda como influência de seu trabalho, seus primeiros desenhos consistiam em ilustrações de águias, âncoras, caveiras, barcos, adagas, andorinhas, pin-ups, pistolas e ciganas, que permanecem até hoje como grande demanda de pedidos em studios no estilo das Tattoos Old School.
